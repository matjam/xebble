std::vector<uint8_t> generate_pixels() {
    // Atlas dimensions: 128x72 (16 chars/row * 8px, 6 rows * 12px)
    std::vector<uint8_t> pixels(ATLAS_W * ATLAS_H * 4, 0);

    for (int ch = 0; ch < NUM_CHARS; ++ch) {
        const uint32_t col = static_cast<uint32_t>(ch) % CHARS_PER_ROW;
        const uint32_t row = static_cast<uint32_t>(ch) / CHARS_PER_ROW;
        const uint32_t base_x = col * GLYPH_W;
        const uint32_t base_y = row * GLYPH_H;

        // 8x8 source glyphs centered in 12px cells with 2px top padding
        constexpr uint32_t Y_OFFSET = 2;

        for (uint32_t gy = 0; gy < 8; ++gy) {
            const uint8_t row_bits = FONT_DATA[ch * 8 + gy];
            for (uint32_t gx = 0; gx < 8; ++gx) {
                if (row_bits & (0x80 >> gx)) {
                    const uint32_t px = base_x + gx;
                    const uint32_t py = base_y + Y_OFFSET + gy;
                    const size_t idx = (py * ATLAS_W + px) * 4;
                    pixels[idx + 0] = 255; // R
                    pixels[idx + 1] = 255; // G
                    pixels[idx + 2] = 255; // B
                    pixels[idx + 3] = 255; // A
                }
            }
        }
    }

    return pixels;
}

std::u8string charset() {
    std::u8string cs;
    cs.reserve(NUM_CHARS);
    for (int ch = FIRST_CHAR; ch <= LAST_CHAR; ++ch) {
        cs.push_back(static_cast<char8_t>(ch));
    }
    return cs;
}

std::expected<std::unique_ptr<BitmapFont>, Error> create_font(vk::Context& ctx) {
    auto pixels = generate_pixels();

    auto texture = Texture::create_from_pixels(ctx, pixels.data(), ATLAS_W, ATLAS_H);
    if (!texture) {
        return std::unexpected(texture.error());
    }

    auto sheet = SpriteSheet::from_texture(std::move(*texture), GLYPH_W, GLYPH_H);
    if (!sheet) {
        return std::unexpected(sheet.error());
    }

    auto font = BitmapFont::from_spritesheet(std::move(*sheet), charset());
    if (!font) {
        return std::unexpected(font.error());
    }

    return std::make_unique<BitmapFont>(std::move(*font));
}

